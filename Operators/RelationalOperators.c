// RELATIONAL/COMPARISON Operators
// < -> less than a<b
// > -> Greater than a>b
// <= -> Less than, equals to a<=b
// >= -> Greater than, equals to a>=b
// == -> Equals to -> a == b
// != -> Not equals to -> a != b
#include <stdio.h>

int main()
{
    int a = 5;
    int b = 5;
    
    printf("a<b = %d", a<b);
    printf("\na>b = %d", a>b);
    printf("\na<=b = %d", a<=b);
    printf("\na>=b = %d", a>=b);
    printf("\na==b = %d", a==b);
    printf("\na!=b = %d", a!=b);

    return 0;
}
